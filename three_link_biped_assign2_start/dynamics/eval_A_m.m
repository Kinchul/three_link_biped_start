function A_m = eval_A_m(q_m)

    [m, ~, m3, l1, l2, l3, ~] = set_parameters();

    q1_m = q_m(1); 
    q2_m = q_m(2);
    q3_m = q_m(3);
   
    t2 = l1.^2;
    t3 = q1_m-q2_m;
    t4 = cos(t3);
    t5 = l1.*l2.*m.*t4.*(1.0./2.0);
    t6 = q1_m-q3_m;
    t7 = cos(t6);
    t8 = q2_m-q3_m;
    t9 = cos(t8);
    t10 = l2.*l3.*m3.*t9.*(1.0./4.0);
    A_m = reshape([t5-m.*t2.*(5.0./4.0)-m3.*t2-l1.*l3.*m3.*t7.*(1.0./2.0),m.*t2.*(1.0./4.0),l1.*l3.*m3.*t7.*(-1.0./2.0),t5+t10-l2.^2.*m.*(1.0./4.0)+l1.*l2.*m3.*t4.*(1.0./2.0),0.0,t10,0.0,0.0,0.0],[3,3]);

end