function A_p = eval_A_p(q_p)
    
    [m, ~, m3, l1, l2, l3, ~] = set_parameters();
    
    q1_p = q_p(1); 
    q2_p = q_p(2);
    q3_p = q_p(3);

    t2 = l1.^2;
    t3 = q1_p-q2_p;
    t4 = cos(t3);
    t5 = l1.*l2.*m.*t4.*(1.0./2.0);
    t6 = q1_p-q3_p;
    t7 = cos(t6);
    A_p = reshape([t5-m.*t2.*(5.0./4.0)-m3.*t2+l1.*l2.*m3.*t4.*(1.0./2.0),m.*t2.*(1.0./4.0),m3.*t2.*(1.0./2.0),t5-l2.^2.*m.*(1.0./4.0),0.0,0.0,l1.*l3.*m3.*t7.*(-1.0./2.0)+l2.*l3.*m3.*cos(q2_p-q3_p).*(1.0./4.0),0.0,l1.*l3.*m3.*t7.*(1.0./4.0)],[3,3]);

end